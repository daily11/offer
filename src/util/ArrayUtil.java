package util;

public class ArrayUtil {

    public static void sop(int[] objects) {
        for(int o : objects){
            System.out.print(o+",");
        }
        System.out.println();
    }

    public static void sop(String[] objects) {
        for(String o : objects){
            System.out.print(o+",");
        }
        System.out.println();
    }

    public static void sop(double[] objects) {
        for(double o : objects){
            System.out.print(o+",");
        }
        System.out.println();
    }

    public static void sop(char[] objects) {
        for(char o : objects){
            System.out.print(o+",");
        }
        System.out.println();
    }
}
